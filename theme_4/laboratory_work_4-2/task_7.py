# На вход программе подается натуральное число n, затем n строк, затем число k — количество поисковых запросов, затем k строк — поисковые запросы. Напишите программу, которая выводит все введенные строки, в которых встречаются все поисковые запросы.

n = int(input("Введите количество строк n: "))
lines = [input(f"Введите строку {i + 1}: ") for i in range(n)]
k = int(input("Введите количество поисковых запросов k: "))
queries = [input(f"Введите поисковый запрос {i + 1}: ").lower() for i in range(k)]
print("\n".join(line for line in lines if all(q in line.lower() for q in queries)))
